    /*
     scale an Image to a given height and width
     */
    private Image scaleIamge(Image img, int w, int h) {

        BufferedImage resizeImage = new BufferedImage(w, h, BufferedImage.TYPE_INT_RGB);
        Graphics2D g = resizeImage.createGraphics();
        g.setRenderingHint(RenderingHints.KEY_INTERPOLATION, RenderingHints.VALUE_INTERPOLATION_NEAREST_NEIGHBOR);
        g.drawImage(img, 0, 0, w, h, null);
        g.dispose();
        return resizeImage;
    }

    /*
     convert a byte[] image into a scaled Image
     */
    private ImageIcon getScaledImage(byte[] blobAsBytes) {
        try {
            BufferedImage bufferedImage = ImageIO.read(new ByteArrayInputStream(blobAsBytes));
            if (bufferedImage != null) {
                ImageIcon resized = new ImageIcon(scaleIamge(bufferedImage, 120, 110));

                return resized;
            }
            return null;
        } catch (IOException ex) {
            Logger.getLogger(Card.class.getName()).log(Level.SEVERE, null, ex);
            return null;
        }
    }

    /*
     convert an Image to BufferedImage
     */
    private static BufferedImage getBufferedImage(Image img) {
        if (img instanceof BufferedImage) {
            return (BufferedImage) img;
        }

        BufferedImage bimage = new BufferedImage(img.getWidth(null),
                img.getHeight(null), BufferedImage.TYPE_INT_ARGB);

        Graphics2D bGr = bimage.createGraphics();
        bGr.drawImage(img, 0, 0, null);
        bGr.dispose();

        return bimage;
    }

    /*
     return a timestamp with today's date (hh:mm:ss = 00:00:00)
     */
    private static Timestamp getToday() {
        java.util.Date now = new java.util.Date();
        return new Timestamp(now.getYear(), now.getMonth(), now.getDate(), 0, 0, 0, 0);
    }

    /*
     batch all given programs to the PreparedStatement
     */
    private void batchPrograms(PreparedStatement st, ArrayList<Object[]> programs,
            long cardNumber) throws SQLException {

        PreparedStatement getZonesToAdd;
        ResultSet zonesToAdd;
        byte maxZoneNumber;
        char length;

        for (Object[] program : programs) {
            // for each program
            Timestamp purchase = (Timestamp) program[1];
            maxZoneNumber = Byte.parseByte(program[3].toString());
            length = program[4].toString().charAt(0);

            getZonesToAdd = (cardType == PAPER)
                    ? con.prepareStatement(Queries.PAPER_PROGRAM_ZONES_TO_ADD)
                    : con.prepareStatement(Queries.OYSTER_PROGRAM_ZONES_TO_ADD);

            getZonesToAdd.setByte(1, maxZoneNumber);
            getZonesToAdd.setLong(2, cardNumber);
            getZonesToAdd.setTimestamp(3, purchase);
            getZonesToAdd.setString(4, String.valueOf(length));

            //set of zones (minZone,maxZone] to add
            zonesToAdd = getZonesToAdd.executeQuery();

            while (zonesToAdd.next()) {
                // for each zone to add
                st.setLong(1, cardNumber);
                st.setTimestamp(2, purchase);
                st.setByte(3, zonesToAdd.getByte("number"));
                st.setString(4, String.valueOf(length));
                st.addBatch();
            }
        }
    }

    /*
     set the card's variables from the DB
     */
    private void setVariables() {
        PreparedStatement st;
        ResultSet rs;
        try {
            st = con.prepareStatement(Queries.SELECT_OYSTER_PICTURE);
            st.setLong(1, cardNumber);

            rs = st.executeQuery();

            if (rs.isBeforeFirst()) {
                // result set not empty - the card is Oyster

                rs.next();
                this.cardType = OYSTER;

                Blob blob = rs.getBlob("picture");
                if (blob != null) {
                    int blobLength = (int) blob.length();
                    byte[] blobAsBytes = blob.getBytes(1, blobLength);
                    this.oldPicture = this.picture = getScaledImage(blobAsBytes);
                }

            } else {
                // result set is empty - the card is Paper
                st = con.prepareStatement(Queries.SELECT_PAPER_ISTOURIST);
                st.setLong(1, cardNumber);

                rs = st.executeQuery();

                rs.next();
                this.cardType = PAPER;
                this.oldIsTourist = this.isTourist = rs.getBoolean("isTourist");
            }
            this.isCardBounded = isCardBounded();

        } catch (SQLException ex) {
            System.out.println("ex = " + ex.getMessage());
            Logger.getLogger(Card.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

