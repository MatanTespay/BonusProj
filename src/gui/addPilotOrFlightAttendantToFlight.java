    private void fillFlights() {
        HelperClass.fillFlightsCombo(this, cbFlights, cbPilots.isVisible());
        setSelectFlightInfo();
        fillStaff();
    }

    private void fillStaff() {
        int flightNumber = -1;
        if (cbFlights.getModel().getSize() > 0) {
            flightNumber = Integer.parseInt(((ComboItem) cbFlights.getSelectedItem()).getValue());
        }

        Flight selectFlight = MainClass.getIfly().getFlights().get(flightNumber);
        if (cbPilots.isVisible()) {
            HelperClass.fillPilotsCombo(this, cbPilots, selectFlight);
        } else if (cbFlightAttendant.isVisible()) {
            HelperClass.fillFlightAttendantCombo(this, cbFlightAttendant, selectFlight);
        }

    }

