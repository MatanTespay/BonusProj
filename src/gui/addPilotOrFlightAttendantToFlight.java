    /**
     * @param evt
     * @exception {@ link exceptions.AddPilotOrFlightAttendantExeption}
     */
    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        // TODO add your handling code here:
        try {
            if (cbFlights.getModel().getSize() <= 0) {
                shoMissingDataMsg("Flights are missing");
                return;
            }
            if (cbPilots.isVisible() && cbPilots.getModel().getSize() <= 0) {
                shoMissingDataMsg("Pilots are missing");
                return;
            }

            if (cbFlightAttendant.isVisible() && cbFlightAttendant.getModel().getSize() <= 0) {
                shoMissingDataMsg("Flight Attendant are missing");
                return;
            }
            
            int flightNumber = Integer.parseInt(((ComboItem) cbFlights.getSelectedItem()).getValue());
            Flight selectFlight = MainClass.getIfly().getFlights().get(flightNumber);
            int employeeNumber;

            if (cbPilots.isVisible()) {
                employeeNumber = Integer.parseInt(((ComboItem) cbPilots.getSelectedItem()).getValue());
            } else if (cbFlightAttendant.isVisible()) {
                employeeNumber = Integer.parseInt(((ComboItem) cbFlightAttendant.getSelectedItem()).getValue());
            } else {
                return;
            }

            Employee emp = MainClass.getIfly().getEmployees().get(employeeNumber);
            MainClass.getIfly().addPilotOrFlightAttendantToFlight(emp, selectFlight);
            JOptionPane.showInternalConfirmDialog(this, "Saved changes", "Confirmation", JOptionPane.PLAIN_MESSAGE, JOptionPane.INFORMATION_MESSAGE);
            setIflyState(false);
        } catch (NumberFormatException | AddPilotOrFlightAttendantExeption ex) {
            JOptionPane.showInternalConfirmDialog(this, ex.getMessage(), "Error", JOptionPane.PLAIN_MESSAGE, JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btnOkActionPerformed

    private void setSelectFlightInfo() {
        if (cbFlights.getModel().getSize() <= 0) {
            return;
        }

        int flightNumber = Integer.parseInt(((ComboItem) cbFlights.getSelectedItem()).getValue());
        Flight selectFlight = MainClass.getIfly().getFlights().get(flightNumber);
        lblSource.setText(selectFlight.getSource().getCity());
        lblDestination.setText(selectFlight.getDestination().getCity());
    }
